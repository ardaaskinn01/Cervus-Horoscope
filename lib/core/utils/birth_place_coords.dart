  /// Returns [latitude, longitude] for the given birth place name.
  /// Falls back to Istanbul (41.0082, 28.9784) if not found.
  static List<double> getCoords(String placeName) {
    // Exact match
    if (_coords.containsKey(placeName)) {
      return _coords[placeName]!;
    }

    // Case-insensitive fallback
    final lower = placeName.toLowerCase().trim();
    for (final entry in _coords.entries) {
      if (entry.key.toLowerCase() == lower) {
        return entry.value;
      }
    }

    // Partial match (e.g. "İstanbul, Türkiye")
    for (final entry in _coords.entries) {
      if (lower.contains(entry.key.toLowerCase()) ||
          entry.key.toLowerCase().contains(lower)) {
        return entry.value;
      }
    }

    // Default: Istanbul
    return [41.0082, 28.9784];
  }
